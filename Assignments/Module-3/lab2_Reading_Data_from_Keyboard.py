# Practical Example 1: Program to Read Name and Age and Print a Formatted Output
name = input("Enter your name: ")
age = int(input("Enter your age: "))

print(f"My name is {name} and I am {age} years old.")

# Practical Example 2: Program to Read a String, Integer, and Float from the Keyboard and Display Them
name = input("Enter a string: ")
number = int(input("Enter an integer: "))
decimal = float(input("Enter a float value: "))

print("String entered:", name)
print("Integer entered:", number)
print("Float entered:", decimal)