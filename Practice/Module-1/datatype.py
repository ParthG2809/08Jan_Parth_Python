# This program demonstrates variables and different data types in Python

# Integer data type
age = 25

# Float data type
height = 5.9

# String data type
name = "Parth"

# Boolean data type
is_student = True

# List data type
marks = [85, 90, 88]

# Display variables and their data types
print("Age:", age, type(age))
print("Height:", height, type(height))
print("Name:", name, type(name))
print("Is Student:", is_student, type(is_student))
print("Marks:", marks, type(marks))
